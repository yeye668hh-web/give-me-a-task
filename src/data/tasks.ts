export const getHotTasks = () => {
  return [...mockTasks].sort((a, b) => b.likes - a.likes).slice(0, 6);
};

export const getRandomTask = (excludeIds: string[] = []) => {
  const availableTasks = mockTasks.filter(task => !excludeIds.includes(task.id));
  if (availableTasks.length === 0) return mockTasks[Math.floor(Math.random() * mockTasks.length)];
  return availableTasks[Math.floor(Math.random() * availableTasks.length)];
};

export const getTaskById = (id: string) => {
  return mockTasks.find(task => task.id === id);
};

export const filterTasks = (
  filters: {
    locations?: string[];
    durations?: string[];
    types?: string[];
    difficulties?: string[];
  }
) => {
  return mockTasks.filter(task => {
    if (filters.locations && filters.locations.length > 0 && !filters.locations.includes(task.location)) {
      return false;
    }
    if (filters.durations && filters.durations.length > 0 && !filters.durations.includes(task.duration)) {
      return false;
    }
    if (filters.types && filters.types.length > 0 && !filters.types.includes(task.type)) {
      return false;
    }
    if (filters.difficulties && filters.difficulties.length > 0 && !filters.difficulties.includes(task.difficulty)) {
      return false;
    }
    return true;
  });
};
